        foreach ($p in 'Adaptive Inter-Frame Spacing','DMA Coalescing','Reduce Speed On Power Down') { Apply-NicProp $p 'Disabled' }
        Apply-NicProp 'Wait for Link'               'Off'
        Apply-NicProp 'Gigabit Master Slave Mode'   'Auto Detect'
    }
}

