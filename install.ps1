function Set-Reg {
    param([string]$Path, [string]$Name, $Value, [string]$Type='DWord')
    if (-not (Test-Path $Path)) { New-Item -Path $Path -Force | Out-Null }
    $old = (Get-ItemProperty -Path $Path -Name $Name -ErrorAction SilentlyContinue).$Name
    if ("$old" -eq "$Value") {
        $backup.Registry["$Path|$Name"] = @{ Old=$old; New=$Value; Type=$Type; Skipped=$true }
        return
    }
    $backup.Registry["$Path|$Name"] = @{ Old=$old; New=$Value; Type=$Type }
    try {
        Set-ItemProperty -Path $Path -Name $Name -Value $Value -Type $Type -Force -ErrorAction Stop
    } catch {
        # If type mismatch, recreate
        try { Remove-ItemProperty -Path $Path -Name $Name -Force -ErrorAction SilentlyContinue } catch {}
        try { New-ItemProperty -Path $Path -Name $Name -Value $Value -PropertyType $Type -Force | Out-Null } catch { W "  [WARN] $Path\$Name -> $Value : $($_.Exception.Message)" DarkYellow }
    }
}

function Disable-SvcSafe {
    param([string]$Name)
    $neverTouch = @('WinDefend','MpsSvc','wuauserv','BFE','EventLog','Sense','SecurityHealthService','SgrmBroker','vgc','vgk','EasyAntiCheat','EasyAntiCheat_EOS','BEService','BEDaisy','FACEITService','DcomLaunch','RpcEptMapper','RpcSs','LSM','Power','PlugPlay')
    if ($Name -in $neverTouch) { W "  [SKIP] $Name (protected)" DarkGray; return }
    $s = Get-Service -Name $Name -ErrorAction SilentlyContinue
    if (-not $s) { W "  [ABSENT] $Name" DarkGray; return }
    $backup.Services[$Name] = @{ Old=$s.StartType.ToString(); Status=$s.Status.ToString() }
    try { Stop-Service -Name $Name -Force -ErrorAction SilentlyContinue } catch {}
    try { Set-Service -Name $Name -StartupType Disabled -ErrorAction Stop; W "  [DISABLED] $Name" Green } catch { W "  [FAIL] $Name : $($_.Exception.Message)" Red }
}

